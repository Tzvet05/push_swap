#include "libft.h"

void	*ft_free_arr(void **arr)
{
	size_t	i;
	void	*ptr;

	i = 0;
	ptr = arr[i];
	while (ptr)
	{
		free(ptr);
		i++;
		ptr = arr[i];
	}
	free(arr);
	return (NULL);
}
